// Language switcher functionality
document.addEventListener('DOMContentLoaded', function() {
  const languageSelector = document.querySelector('.language-selector');
  const selectedLanguage = languageSelector.querySelector('.selected-language');
  const languageDropdown = languageSelector.querySelector('.language-dropdown');
  
  // Set initial language
  let currentLanguage = localStorage.getItem('language') || 'en';
  updatePageLanguage(currentLanguage);
  
  // Set initial selected language display
  const initialLang = languageDropdown.querySelector(`[data-lang="${currentLanguage}"]`);
  if (initialLang) {
    const flag = initialLang.querySelector('img').src;
    const text = initialLang.querySelector('span').textContent;
    selectedLanguage.querySelector('img').src = flag;
    selectedLanguage.querySelector('span').textContent = text;
  }
  
  // Toggle dropdown
  selectedLanguage.addEventListener('click', function(e) {
    e.stopPropagation();
    languageSelector.classList.toggle('active');
  });
  
  // Handle language selection
  languageDropdown.querySelectorAll('li').forEach(item => {
    item.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      const flag = this.querySelector('img').src;
      const text = this.querySelector('span').textContent;
      
      // Update selected language display
      selectedLanguage.querySelector('img').src = flag;
      selectedLanguage.querySelector('span').textContent = text;
      
      // Update page language
      updatePageLanguage(lang);
      localStorage.setItem('language', lang);
      
      // Close dropdown
      languageSelector.classList.remove('active');
    });
  });
  
  // Close dropdown when clicking outside
  document.addEventListener('click', function() {
    languageSelector.classList.remove('active');
  });
});

function updatePageLanguage(lang) {
  // Get all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    
    if (translations[lang] && translations[lang][key]) {
      // Handle different element types
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.value = translations[lang][key];
      } else if (element.tagName === 'IMG') {
        element.alt = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
}